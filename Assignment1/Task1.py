
#! Task 1: Perform Basic Mathematical Operations
#! Problem Statement: Write a Python program that does the following:
#! 1.  Takes two numbers as input from the user.
#! 2.  Performs the basic mathematical operations on these two numbers:
#! o	Addition
#! o	Subtraction
#! o	Multiplication
#! o	Division
#! 3.  Displays the results of each operation on the screen.

"""Program to perform basic mathematical operations on two numbers."""
num1=int(input("Enter first number: "))
num2=int(input("Enter second number: "))
addition=num1+num2
subtraction=num1-num2
multiplication=num1*num2
division=num1/num2
print("Addition of two numbers is: ",addition)
print("Subtraction of two numbers is: ",subtraction)
print("Multiplication of two numbers is: ",multiplication)
print("Division of two numbers is: ",division)
#~ Sample Input:
#~ Enter first number: 10
#~ Enter second number: 5
#~ Sample Output:
#~ Addition of two numbers is: 15
#~ Subtraction of two numbers is: 5
#~ Multiplication of two numbers is: 50
#~ Division of two numbers is: 2.0
#~ Explanation:
#~ The program takes two numbers as input from the user and performs addition, subtraction, multiplication, and division on these numbers. It then displays the results of each operation on the screen.


